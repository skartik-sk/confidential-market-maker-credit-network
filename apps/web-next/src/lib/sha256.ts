/**
 * Browser-safe cryptographic primitives.
 *
 * Pure-JS SHA-256 (synchronous) + secure random bytes + hex helpers.
 * No `node:crypto` dependency, so these work identically in the browser
 * client bundle and in Node/edge server code.
 */

function utf8(str: string): Uint8Array {
  return new TextEncoder().encode(str);
}

export function toHex(bytes: Uint8Array): string {
  let out = "";
  for (let i = 0; i < bytes.length; i++) out += bytes[i].toString(16).padStart(2, "0");
  return out;
}

export function fromHex(hex: string): Uint8Array {
  const out = new Uint8Array(hex.length / 2);
  for (let i = 0; i < out.length; i++) out[i] = parseInt(hex.substr(i * 2, 2), 16);
  return out;
}

